    private void basvur_butonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_basvur_butonActionPerformed
           String kullaniciAdi=JOptionPane.showInputDialog("Kullanıcı adınızı giriniz");
           int selectedrow = ilan_tablosu.getSelectedRow();
            if (selectedrow == -1) {          
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("İlanlar tablosu şu anda boş. ");
            }
            else {
                mesaj_yazisi.setText("Lütfen başvurulacak bir ilan seçin.");
            }
        }
        else {      
            int id = (int)model.getValueAt(selectedrow,0);    
            islemler.basvuruEkle(kullaniciAdi, id);
            isIlaniGoruntule();    
            mesaj_yazisi.setText("Başvurunuz alınmıştır");
        }
    }//GEN-LAST:event_basvur_butonActionPerformed

