    public void isIlaniGoruntule(){
               
        model.setRowCount(0);        
        ArrayList<IsIlani> ilanlar = new ArrayList<IsIlani>();              
        ilanlar = islemler.isIlaniGetir();
        
        if (ilanlar != null ) {
            
            for (IsIlani isIlani : ilanlar) {
                Object[] eklenecek = {isIlani.getId(),isIlani.getDepartman(),isIlani.getPozisyon(),isIlani.getKonum()};               
                model.addRow(eklenecek);             
            }           
        }        
    }

